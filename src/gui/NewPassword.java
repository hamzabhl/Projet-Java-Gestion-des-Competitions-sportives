    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        String password = txtPassword.getText();
        String confirmPassword = txtConfirmPassword.getText();

        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match!");
            return;
        }

        try {
            Admin admin = as.findByLogin(EmailField.email); // Use the field from constructor

            System.out.println(password);
            System.out.println(security);
            JOptionPane.showConfirmDialog(rootPane, "Confirm");
            if (admin != null) {
                admin.setPassword(AdminService.hashPasswordMD5(password + security));
                as.update(admin);
                JOptionPane.showMessageDialog(this, "Password updated successfully!");
                this.setVisible(false);// Close the frame
                Main m = new Main();
                m.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "No admin found with that email.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

    }//GEN-LAST:event_btnResetActionPerformed

