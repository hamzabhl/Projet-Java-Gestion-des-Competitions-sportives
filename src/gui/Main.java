    private void bnConnexionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnConnexionActionPerformed
        String login = txtLogin.getText().trim();
        String password = txtPassword.getText().trim();

        if (login.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs!", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Admin admin = as.findByLogin(login);

        if (admin != null) {
            //security = "H70K3lm85";
            String hashedPassword = hashPasswordMD5(password + security);
            if (login.equals(admin.getLogin()) && hashedPassword.equals(admin.getPassword())) {
                MDIApplication mdi = new MDIApplication();
                mdi.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Incorrect Login ou Password!", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Utilisateur introuvable!", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bnConnexionActionPerformed

