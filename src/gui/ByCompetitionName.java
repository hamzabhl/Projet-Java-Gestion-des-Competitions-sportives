public ByCompetitionName() {
    super("Student % by Competition", true, true, true, true);
    setSize(1200, 900);
    setFrameIcon(new ImageIcon(getClass().getResource("/icons/balls.png")));

    // Fetch data from service
    InscriptionService service = new InscriptionService();
    Map<String, Integer> dataMap = service.getStudentCountByCompetition();

    DefaultPieDataset dataset = new DefaultPieDataset();
    for (Map.Entry<String, Integer> entry : dataMap.entrySet()) {
        dataset.setValue(entry.getKey(), entry.getValue());
    }

    // Create chart
    JFreeChart chart = ChartFactory.createPieChart(
            "Student % by Competition",
            dataset,
            true,
            true,
            false
    );

    PiePlot plot = (PiePlot) chart.getPlot();

    // âœ… Show percentage in labels
    PieSectionLabelGenerator labelGenerator = new org.jfree.chart.labels.StandardPieSectionLabelGenerator(
            "{0} ({2})",
            NumberFormat.getNumberInstance(), new DecimalFormat("0.0%")
    );
    plot.setLabelGenerator(labelGenerator);

    // ðŸŽ¨ Custom colors
    plot.setSectionPaint("Football", new Color(0x000000));
    plot.setSectionPaint("Basket-ball", new Color(0x2196F3));
    plot.setSectionPaint("Gymnastique", new Color(0xFF9800));
    plot.setSectionPaint("Saut en longeur", new Color(0xfffe00));
    plot.setSectionPaint("Hand-ball", new Color(0xb1ff00));
    plot.setSectionPaint("Volley-ball", new Color(0xb1ff00));
    plot.setSectionPaint("Badminton", new Color(0xff005e));

    ChartPanel chartPanel = new ChartPanel(chart);
    setContentPane(chartPanel);
}


