package entities;

import java.sql.Date;

/**
 *
 * @author hamza
 */
public class Competition {

    private int id;
    private String nom;
    private Date date;
    private String lieu;
    private String type;

    public Competition(String nom, Date date, String lieu, String type) {
        this.nom = nom;
        this.date = date;
        this.lieu = lieu;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getLieu() {
        return lieu;
    }

    public void setLieu(String lieu) {
        this.lieu = lieu;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

}
